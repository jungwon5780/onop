function createDayButtons(buttons, containerId) {
    const container = document.getElementById(containerId);
    const btnContent = document.createElement('div');
    btnContent.className = 'btn_content';

    buttons.forEach((button, index) => {
        // 1. group이 있으면: (오전과제/오후과제 같은 그룹)
        if (button.group && button.buttons) {
            // 그룹 버튼 (toggle 버튼 역할)
            const toggleDiv = document.createElement('div');
            toggleDiv.className = 'day_btn';
            toggleDiv.style.backgroundColor = '#444';

            const icon = document.createElement('i');
            icon.className = 'fa-solid fa-link';
            icon.style.marginRight = '5px';

            const toggleLink = document.createElement('a');
            toggleLink.className = `btn toggle-btn${index + 1}`; // index에 따라 번호 다르게
            toggleLink.href = '#';
            toggleLink.textContent = button.group;

            toggleDiv.appendChild(icon);
            toggleDiv.appendChild(toggleLink);
            toggleDiv.appendChild(document.createElement('br'));
            btnContent.appendChild(toggleDiv);

            // 하위 버튼들 (sub_buttons)
            const subContainer = document.createElement('div');
            subContainer.className = `sub_buttons${index + 1} hidden`;
            subContainer.style.marginLeft = '20px';

            button.buttons.forEach(subBtn => {
                const dayBtn = document.createElement('div');
                dayBtn.className = 'day_btn';

                const subIcon = document.createElement('i');
                subIcon.className = 'fa-solid fa-link';
                subIcon.style.marginRight = '5px';

                const subLink = document.createElement('a');
                subLink.className = 'btn';
                subLink.href = subBtn.href;
                subLink.textContent = subBtn.text;

                dayBtn.appendChild(subIcon);
                dayBtn.appendChild(subLink);
                dayBtn.appendChild(document.createElement('br'));

                subContainer.appendChild(dayBtn);
            });

            btnContent.appendChild(subContainer);
        } else {
            // 2. group 없는 일반 버튼들
            const dayBtn = document.createElement('div');
            dayBtn.className = 'day_btn';

            const icon = document.createElement('i');
            icon.className = 'fa-solid fa-link';
            icon.style.marginRight = '5px';

            const link = document.createElement('a');
            link.className = 'btn';
            link.href = button.href;
            link.textContent = button.text;

            dayBtn.appendChild(icon);
            dayBtn.appendChild(link);
            dayBtn.appendChild(document.createElement('br'));

            btnContent.appendChild(dayBtn);
        }
    });

    container.appendChild(btnContent);
}


